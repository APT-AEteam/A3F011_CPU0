////通过E906写入E902程序，并使E902开始运行(PA6 toggle)
extern uint8_t code_data[1924];
void bc_drive_lc_run_test(void)
{
	csi_pin_set_mux(PA6, PA6_OUTPUT);		//PA06 output
	csi_pin_set_high(PA6);					//PA06 output high;
	
	{
		//disable reset CPU1
		*(unsigned int *)(0x80000008) |=  0x1 <<24; //GCDR
		//enable CPU1 CLK
		*(unsigned int *)(0x80000004) |=  0x1 <<25; //GCER
	}
	
	for(uint32_t i =0; i<=1924 ;i++)
	{  //write E902 CODE DATA
		*(unsigned int *)(0x90000000+i) = code_data[i];
	}
	
	
	{
		//reset CPU1
		*(unsigned int *)(0x80000004) |=  0x1 <<24; //GCER

		
	}
}

